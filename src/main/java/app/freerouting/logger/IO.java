package app.freerouting.logger;

public class IO {
    public static void println(String s) {
        System.out.println(s);
    }

    public static void println() {
        System.out.println();
    }

    public static void print(String s) {
        System.out.print(s);
    }

    public static void print(int i) {
        System.out.print(i);
    }
}
